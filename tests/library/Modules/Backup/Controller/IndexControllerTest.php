namespace Modules\Backup\Controller;

use Modules\Backup\Bootstrap;
use Gc\Registry;

/**
 * Generated by PHPUnit_SkeletonGenerator 1.2.0 on 2013-01-30 at 08:29:30.
 *
 * @group Modules
 * @category Gc_Tests
 * @package  Modules
 */
class IndexControllerTest extends \PHPUnit_Framework_TestCase
{
    /**
     * @var IndexController
     *
     * @return void
     */
    protected $object;

    /**
     * @var Bootstrap
     *
     * @return void
     */
    protected $boostrap;

    /**
     * Sets up the fixture, for example, opens a network connection.
     * This method is called before a test is executed.
     *
     * @return void
     */
    protected function setUp()
    {
        $this->boostrap = new Bootstrap(array());
        $this->boostrap->install();
        $this->object = new IndexController(
            Registry::get('Application')->getRequest(),
            Registry::get('Application')->getResponse()
        );
        $this->object->setServiceLocator($this->getMock('Zend\ServiceManager\ServiceManager'));

    }

    /**
     * Tears down the fixture, for example, closes a network connection.
     * This method is called after a test is executed.
     *
     * @return void
     */
    protected function tearDown()
    {
        $this->boostrap->uninstall();
        unset($this->object);
        unset($this->boostrap);
    }

    /**
     * Test
     *
     * @covers Modules\Backup\Controller\IndexController::indexAction
     *
     * @return void
     */
    public function testIndexAction()
    {
        $this->assertNull($this->object->indexAction());
    }

    /**
     * Test
     *
     * @covers Modules\Backup\Controller\IndexController::downloadDatabaseAction
     *
     * @return void
     */
    public function testDownloadDatabaseActionWithPgsql()
    {
        $configuration = Registry::get('Application')->getConfig();
        if ($configuration['db']['driver'] != 'pdo_pgsql') {
            $this->markTestSkipped('Download the database depends on Pgsql');
        }

        $this->assertInstanceOf('Zend\Http\Response', $this->object->downloadDatabaseAction());
    }

    /**
     * Test
     *
     * @covers Modules\Backup\Controller\IndexController::downloadDatabaseAction
     *
     * @return void
     */
    public function testDownloadActionWithMysql()
    {
        $configuration = Registry::get('Application')->getConfig();
        if ($configuration['db']['driver'] != 'pdo_mysql') {
            $this->markTestSkipped('Download the database depends on Mysql');
        }

        $this->assertInstanceOf('Zend\Http\Response', $this->object->downloadDatabaseAction());
    }

    /**
     * Test
     *
     * @covers Modules\Backup\Controller\IndexController::downloadFilesAction
     *
     * @return void
     */
    public function testDownloadFilesActionWithPgsql()
    {
        $this->assertInstanceOf('Zend\Http\Response', $this->object->downloadFilesAction());
    }
}

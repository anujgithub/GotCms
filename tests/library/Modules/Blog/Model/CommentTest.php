class CommentTest extends \PHPUnit_Framework_TestCase
{
    /**
     * @var Comment
     *
     * @return void
     */
    protected $object;

    /**
     * @var Bootstrap
     *
     * @return void
     */
    protected $boostrap;

    /**
     * Sets up the fixture, for example, opens a network connection.
     * This method is called before a test is executed.
     *
     * @return void
     */
    protected function setUp()
    {
        $this->boostrap = new Bootstrap();
        $this->boostrap->install();
        $this->object = new Comment;
    }

    /**
     * Tears down the fixture, for example, closes a network connection.
     * This method is called after a test is executed.
     *
     * @return void
     */
    protected function tearDown()
    {
        unset($this->object);
    }

    /**
     * Test
     *
     * @covers Modules\Blog\Model\Comment::getDocumentList
     * @covers Modules\Blog\Model\Comment::add
     *
     * @return void
     */
    public function testGetDocumentList()
    {
        $data = array(
            'message' => 'test',
            'username' => 'test',
            'email' => 'pierre.rambaud86@gmail.com',
        );
        $this->object->add($data, $this->document->getId());
        $this->assertInternalType('array', $this->object->getDocumentList());
    }

    /**
     * Test
     *
     * @covers Modules\Blog\Model\Comment::getList
     *
     * @return void
     */
    public function testGetList()
    {
        $this->assertInternalType('array', $this->object->getList(1));
    }

    /**
     * Test
     *
     * @covers Modules\Blog\Model\Comment::add
     *
     * @return void
     */
    public function testAdd()
    {
        $data = array(
            'message' => 'test',
            'username' => 'test',
            'email' => 'pierre.rambaud86@gmail.com',
        );
        $this->assertTrue($this->object->add($data, $this->document->getId()));
    }

    /**
     * Test
     *
     * @covers Modules\Blog\Model\Comment::add
     *
     * @return void
     */
    public function testAddWithWrongValues()
    {
        $data = array(
            'message' => '',
            'username' => '',
            'email' => '',
        );
        $this->assertFalse($this->object->add($data, $this->document->getId()));
    }
}

class NavigationTest extends \PHPUnit_Framework_TestCase
{
    /**
     * @var Navigation
     *
     * @return void
     */
    protected $object;

    /**
     * Sets up the fixture, for example, opens a network connection.
     * This method is called before a test is executed.
     *
     * @covers Gc\Component\Navigation::__construct
     *
     * @return void
     */
    protected function setUp()
    {
        $this->object = new Navigation;
    }

    /**
     * Tears down the fixture, for example, closes a network connection.
     * This method is called after a test is executed.
     *
     * @return void
     */
    protected function tearDown()
    {
        unset($this->object);
    }

    /**
     * Test
     *
     * @covers Gc\Component\Navigation::setBasePath
     *
     * @return void
     */
    public function testSetBasePath()
    {
        $this->object->setBasePath('/base/path');
        $this->assertEquals('/base/path', $this->object->getBasePath());
    }

    /**
     * Test
     *
     * @covers Gc\Component\Navigation::getBasePath
     *
     * @return void
     */
    public function testGetBasePath()
    {
        $this->object->setBasePath('/base/path');
        $this->assertEquals('/base/path', $this->object->getBasePath());
    }

    /**
     * Test
     *
     * @covers Gc\Component\Navigation::render
     *
     * @return void
     */
    public function testRender()
    {
        $document = $this->getMock('Gc\Document\Model');
        $document->expects($this->any())
            ->method('isPublished')
            ->will($this->returnValue(true));
        $document->expects($this->any())
            ->method('getChildren')
            ->will($this->returnValue(array($this->getMock('Gc\Document\Model'))));

        $this->assertTrue(count($this->object->render(array($document))) > 0);
    }

    /**
     * Test
     *
     * @covers Gc\Component\Navigation::render
     *
     * @return void
     */
    public function testRenderWithParameters()
    {
        $this->assertInternalType('array', $this->object->render());
    }
}

namespace Gc\Datatype;

/**
 * Generated by PHPUnit_SkeletonGenerator 1.2.0 on 2012-10-17 at 20:40:10.
 *
 * @group Gc
 * @category Gc_Tests
 * @package  Library
 */
class AbstractDatatypeTest extends \PHPUnit_Framework_TestCase
{
    /**
     * @var AbstractDatatype
     *
     * @return void
     */
    protected $object;

    /**
     * Sets up the fixture, for example, opens a network connection.
     * This method is called before a test is executed.
     *
     * @return void
     */
    protected function setUp()
    {
        $this->viewHelperManager = $this->getMock('Zend\View\HelperPluginManager', array(), array(), '', false);
        $this->viewHelperManager->expects($this->any())
            ->method('get')
            ->with($this->equalTo('url'))
            ->will($this->returnValue($this->getMock('Zend\View\Helper\Url', array('__invoke'))));

        $this->object = $this->getMockForAbstractClass('Gc\Datatype\AbstractDatatype');
        $this->object->load($this->viewHelperManager, $this->getMock('Gc\Datatype\Model'), 1);
    }

    /**
     * Tears down the fixture, for example, closes a network connection.
     * This method is called after a test is executed.
     *
     * @return void
     */
    protected function tearDown()
    {
        unset($this->object);
    }

    /**
     * Test
     *
     * @covers Gc\Datatype\AbstractDatatype::getInfos
     *
     * @return void
     */
    public function testGetInfos()
    {
        $this->assertFalse($this->object->getInfos());
    }

    /**
     * Test
     *
     * @covers Gc\Datatype\AbstractDatatype::load
     *
     * @return void
     */
    public function testLoad()
    {
        $this->assertEquals(1, $this->object->getDocumentId());
        $this->assertInstanceOf('Gc\Datatype\Model', $this->object->getDatatypeModel());
    }

    /**
     * Test
     *
     * @covers Gc\Datatype\AbstractDatatype::load
     *
     * @return void
     */
    public function testLoadWithEmptyDatatype()
    {
        $this->assertFalse($this->object->load($this->viewHelperManager, null, null));
    }

    /**
     * Test
     *
     * @covers Gc\Datatype\AbstractDatatype::getConfig
     *
     * @return void
     */
    public function testGetConfig()
    {
        $datatypeModel = $this->object->getDatatypeModel();
        $datatypeModel->expects($this->any())
            ->method('getData')
            ->with($this->equalTo('prevalue_value'))
            ->will($this->returnValue('s:4:"test";'));

        $this->assertEquals('test', $this->object->getConfig());
    }

    /**
     * Test
     *
     * @covers Gc\Datatype\AbstractDatatype::getConfig
     *
     * @return void
     */
    public function testGetConfigWithNotSerializeValue()
    {
        $datatypeModel = $this->object->getDatatypeModel();
        $datatypeModel->expects($this->any())
            ->method('getData')
            ->with($this->equalTo('prevalue_value'))
            ->will($this->returnValue(array()));
        $this->assertInternalType('array', $this->object->getConfig());
    }

    /**
     * Test
     *
     * @covers Gc\Datatype\AbstractDatatype::setConfig
     *
     * @return void
     */
    public function testSetConfig()
    {
        $this->assertInstanceOf('Gc\Datatype\AbstractDatatype', $this->object->setConfig(serialize('NewValue')));
    }

    /**
     * Test
     *
     * @covers Gc\Datatype\AbstractDatatype::getUploadUrl
     *
     * @return void
     */
    public function testGetUploadUrl()
    {
        $this->assertNUll($this->object->getUploadUrl(1));
    }

    /**
     * Test
     *
     * @covers Gc\Datatype\AbstractDatatype::getHelper
     *
     * @return void
     */
    public function testGetHelper()
    {
        $this->assertInstanceOf('Zend\View\Helper\Url', $this->object->getHelper('url'));
    }

    /**
     * Test
     *
     * @covers Gc\Datatype\AbstractDatatype::getProperty
     *
     * @return void
     */
    public function testGetProperty()
    {
        $this->assertNull($this->object->getProperty());
    }

    /**
     * Test
     *
     * @covers Gc\Datatype\AbstractDatatype::setProperty
     *
     * @return void
     */
    public function testSetProperty()
    {
        $this->object->setProperty('test');
        $this->assertEquals('test', $this->object->getProperty());
    }

    /**
     * Test
     *
     * @covers Gc\Datatype\AbstractDatatype::getName
     *
     * @return void
     */
    public function testGetName()
    {
        $this->assertEquals('datatype', $this->object->getName());
    }

    /**
     * Test
     *
     * @covers Gc\Datatype\AbstractDatatype::addPath
     *
     * @return void
     */
    public function testAddPath()
    {
        $this->assertInstanceOf('Gc\Datatype\AbstractDatatype', $this->object->addPath(__DIR__));
    }

    /**
     * Test
     *
     * @covers Gc\Datatype\AbstractDatatype::addPath
     * @covers Gc\Datatype\AbstractDatatype::render
     *
     * @return void
     */
    public function testRender()
    {
        $this->object->addPath(__DIR__);
        $this->assertEquals('String' . PHP_EOL, $this->object->render('_files/template.phtml'));
    }

    /**
     * Test
     *
     * @covers Gc\Datatype\AbstractDatatype::getDocument
     *
     * @return void
     */
    public function testGetDocument()
    {
        $this->assertFalse($this->object->getDocument());
    }
}

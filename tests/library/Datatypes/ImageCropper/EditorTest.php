            'Datatypes\ImageCropper\Datatype',
            array(
                'getName',
                'getProperty',
                'getConfig',
                'getDocument',
                'getHelper',
            )
        );
        $datatype->expects($this->any())
            ->method('getName')
            ->will($this->returnValue('name'));
        $datatype->expects($this->any())
            ->method('getProperty')
            ->will($this->returnValue($this->property));
        $datatype->expects($this->any())
            ->method('getConfig')
            ->will($this->returnValue($this->getConfig()));

        $this->document = $this->getMock('Gc\Document\Model', array('getId'));
        $this->document->expects($this->any())
            ->method('getId')
            ->will($this->returnValue(1));
        $datatype->expects($this->any())
            ->method('getDocument')
            ->will($this->returnValue($this->document));

        $helperManager = $this->getMock('Zend\View\HelperManager', array('appendFile', 'appendStylesheet'));
        $helperManager->expects($this->any())
            ->method('appendFile')
            ->will($this->returnSelf());
        $helperManager->expects($this->any())
            ->method('appendStylesheet')
            ->will($this->returnSelf());

        $datatype->expects($this->any())
            ->method('getHelper')
            ->will($this->returnValue($helperManager));


        $this->object = new Editor($datatype);
    }

    /**
     * Tears down the fixture, for example, closes a network connection.
     * This method is called after a test is executed.
     *
     * @return void
     */
    protected function tearDown()
    {
        $_FILES = array();
        unset($this->object);
    }

    /**
     * Test
     *
     * @covers Datatypes\ImageCropper\Editor::save
     *
     * @return void
     */
    public function testSave()
    {
        $_FILES = array(
            'name1' => array(
                'name' => __DIR__ . '/_files/test.jpg',
                'type' => 'plain/text',
                'size' => 8,
                'tmp_name' => __DIR__ . '/_files/test.jpg',
                'error' => 0
            )
        );

        $this->object->save();
        $this->assertFileExists(GC_MEDIA_PATH . '/files/1/1');
        $this->removeDirectories();
    }

    /**
     * Test
     *
     * @covers Datatypes\ImageCropper\Editor::save
     *
     * @return void
     */
    public function testSaveWithBmp()
    {
        copy(__DIR__ . '/_files/test-source.bmp', __DIR__ . '/_files/test.bmp');
        $_FILES = array(
            'name1' => array(
                'name' => __DIR__ . '/_files/test.bmp',
                'type' => 'plain/text',
                'size' => 8,
                'tmp_name' => __DIR__ . '/_files/test.bmp',
                'error' => 0
            )
        );

        $this->object->save();
        $this->assertInternalType('string', $this->object->getValue());
        $this->removeDirectories();
    }

    /**
     * Test
     *
     * @covers Datatypes\ImageCropper\Editor::save
     *
     * @return void
     */
    public function testSaveWithEmptyFilesVar()
    {
        $data = serialize(
            array(
                'original' => array(
                    'value' => '/media/files/test/test.jpg',
                    'width' => 10,
                    'height' => 10,
                    'html' => 2,
                    'mime' => 'image/jpeg',
                ),
                '223x112' => array(
                    'value' => '/media/files/test/test-223x112.jpg',
                    'width' => 223,
                    'height' => 112,
                    'html' => 2,
                    'mime' => 'image/jpeg',
                    'x' => 0,
                    'y' => 0,
                ),
                '800x600' => array(
                    'value' => '/media/files/test/test.jpg',
                    'width' => 223,
                    'height' => 112,
                    'html' => 2,
                    'mime' => 'image/jpeg',
                    'x' => 0,
                    'y' => 0,
                ),
            )
        );

        $this->property->expects($this->any())
            ->method('getConfig')
            ->will($this->returnValue(serialize($data)));

        $this->object->getRequest()->getPost()->set('name1-hidden', $data);
        $this->assertNull($this->object->save());
        $this->removeDirectories();
    }

